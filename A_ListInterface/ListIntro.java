        /*
            Java List Interface:
            In java, the List interface is an ordered collection that allows us to store and access elements sequentially.

            We can not create object of List because it's an Interface
            Example:
            List l1 = new List();   <- Wrong

            Instead:
            List l1 = new ArrayList();   <- Right
            List l1 = new LinkedList();   <- Right

            We provide the implementation of SubClass or Concrete class to List reference variable - (upcasting)


            SubClass or Concrete class of List:
            1. ArrayList
            2. LinkedList
            3. Stack
            4. Vector

        */
